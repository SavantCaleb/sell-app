// Simple script to generate placeholder images
const fs = require('fs');

// Create a simple 1x1 pixel PNG (transparent)
const pngData = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
  0x00, 0x00, 0x00, 0x0d, // IHDR length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x01, // width: 1
  0x00, 0x00, 0x00, 0x01, // height: 1
  0x08, 0x06, // bit depth: 8, color type: 6 (RGBA)
  0x00, 0x00, 0x00, // compression, filter, interlace
  0x1f, 0x15, 0xc4, 0x89, // CRC
  0x00, 0x00, 0x00, 0x0d, // IDAT length
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x78, 0x9c, 0x62, 0x00, 0x00, 0x00, 0x00, 0x05, 0x00, 0x01, // compressed data
  0x00, 0x00, 0x00, 0x00, // CRC
  0x49, 0x45, 0x4e, 0x44, // IEND
  0xae, 0x42, 0x60, 0x82  // CRC
]);

// Create placeholder images
const files = ['splash.png', 'icon.png', 'adaptive-icon.png', 'favicon.png'];

files.forEach(file => {
  fs.writeFileSync(`./assets/${file}`, pngData);
  console.log(`Created ${file}`);
});